/**
 * Parse CSV text into draw arrays.
 * Expects one draw per line, numbers separated by commas, spaces, or tabs.
 * Lines starting with # or empty lines are skipped.
 * Handles headers: if the first token in a line is not a number, the line is skipped.
 */
export const parseCSVDraws = (text: string, numbersPerDraw: number): number[][] => {
  const lines = text.split('\n').map((l) => l.trim()).filter((l) => l && !l.startsWith('#'));
  const draws: number[][] = [];

  for (const line of lines) {
    const parts = line.split(/[,\t\s]+/).map((s) => s.trim()).filter((s) => s.length > 0);
    const nums = parts.map(Number).filter((n) => !isNaN(n) && n >= 1);
    if (nums.length >= numbersPerDraw) {
      draws.push(nums.slice(0, numbersPerDraw).sort((a, b) => a - b));
    }
  }

  return draws;
};